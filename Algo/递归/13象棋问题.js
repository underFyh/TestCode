// 横线9条（y）、纵线10条(x)
function process(x, y, rest, a, b) {
    // 跳出越界了则说明行不通
    if (x < 0 || x > 9 || y < 0 || y > 8) return 0;
    // 基础情况
    if (rest === 0) {
        if (x === a && y === b) {
            return 1
        } else {
            return 0
        }
    }

    let ways = 0;
    ways += process(x + 1, y + 2, rest - 1, a, b);
    ways += process(x + 2, y + 1, rest - 1, a, b);
    ways += process(x + 2, y - 1, rest - 1, a, b);
    ways += process(x + 1, y - 2, rest - 1, a, b);
    ways += process(x - 1, y - 2, rest - 1, a, b);
    ways += process(x - 2, y - 1, rest - 1, a, b);
    ways += process(x - 2, y + 1, rest - 1, a, b);
    ways += process(x - 1, y + 2, rest - 1, a, b);

    return ways;
}

