function dp(a, b, k) {

    // 1.范围确定： x范围是0-9  y的范围是0-8  rest范围0-10
    let dp = Array.from(
        {length: 10},
        () => Array.from({length: 9},
            () => Array.from({length: k + 1}, () => 0)),
    )

    // 2. 基础情况 rest === 0  x === a && y === b
    dp[a][b][0] = 1;


    // 4. 有了第0层rest，就可以填写第一层rest，全部枚举求出值
    for (let rest = 1; rest <= k; rest++) {
        for (let x = 0; x < 10; x++) {
            for (let y = 0; y < 9; y++) {
                let ways = 0;
                ways += pick(dp, x + 1, y + 2, rest - 1);
                ways += pick(dp, x + 2, y + 1, rest - 1);
                ways += pick(dp, x + 2, y - 1, rest - 1);
                ways += pick(dp, x + 1, y - 2, rest - 1);
                ways += pick(dp, x - 1, y - 2, rest - 1);
                ways += pick(dp, x - 2, y - 1, rest - 1);
                ways += pick(dp, x - 2, y + 1, rest - 1);
                ways += pick(dp, x - 1, y + 2, rest - 1);
                dp[x][y][rest] = ways;
            }
        }
    }

    // 3. 返回值递归传入的为 0 0 rest
    return dp[0][0][k];
}

function pick(dp, x, y, rest) {
    // 如果越界则返回0
    if (x < 0 || x > 9 || y < 0 || y > 8) return 0;
    return dp[x][y][rest];
}


